<script>

    $(document).ready(function () {
        if ($('.actively.not').is(':checked')) {
            $('.actively-area').hide();
        } else {
            $('.actively-area').show();
        }
    });

    $('.actively').on('change', function () {
        if ($(this).hasClass('not')) {
            $('.actively-area').hide();
        } else {
            $('.actively-area').show();
        }
    });

    //Add New
    $('.addNew').on('click', function () {
        $('.addNew,.search-area,.details-cards').slideUp('fast', function () {

            $('.addForm').slideDown('slow');

        });
    });

    //Close Form
    $('.btn_close').click(function (e) {
        $('.addForm').slideUp('fast', function () {
            $('.search-area,.details-cards').slideDown('slow', function () {
                $('.addNew').slideDown('fast');
            });
        })
    });

    //Card expand script
    $(function () {
        $('.btn_expand').on('click', function () {
            var $this = $(this);
            var card = $this.parents('.expand-card');

            if (!$this.hasClass('expand')) {
                $this.addClass('expand').html('expand_less');
                card.find('.expand-card-content').slideDown('fast');
                $('ul.tabs').tabs();
            } else {
                $this.removeClass('expand').html('expand_more');
                card.find('.expand-card-content').slideUp('fast');
            }
        });
    });

    $('.datepicker').pickadate({
        selectMonths: true,
        selectYears: 15,
        today: 'Today',
        clear: 'Clear',
        close: 'Ok',
        closeOnSelect: false
    });


    $(document).ready(function () {
        $('select').material_select();
    });


    //===================================================================
    //    Backend Script
    //===================================================================

    $('.addProQualification').on('click', function () {
        $.ajax({
            type: 'GET',
            url: "<?php echo Yii::app()->baseUrl . '/Admin/Candidate/QualificationInput'; ?>",
            success: function (html) {
                $('.qualification-content').append(html);
            }
        });
    });

    $('.addMembership').on('click', function () {
        $.ajax({
            type: 'GET',
            url: "<?php echo Yii::app()->baseUrl . '/Admin/Candidate/Membership'; ?>",
            success: function (html) {
                $('.membership-content').append(html);
            }
        });
    });


</script>
